package Day2;

public class EscapeSequences {

    public static void main(String[] args) {

        // To be able to use double quotes in a sentence we need backslash
        System.out.println("I learn \"Java\" from TechnoStudy");

        // To be able to use backslash in a sentence we need backslash
        System.out.println("C:\\ProgramFiles\\..");

        // To move a new line - \n
        System.out.println("Java is a very good \nprogramming language");

        // To use tab - \t
        System.out.println("\tTechnostudy");
        System.out.println("Technostudy");

    }

}

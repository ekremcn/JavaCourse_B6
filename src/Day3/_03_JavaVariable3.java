package Day3;

public class _03_JavaVariable3 {

    public static void main(String[] args) {

        // you can create variables in the same line
        // BUT I DO NOT RECOMMEND!!!
        int year = 2022, month = 4;

        System.out.println(year);
        System.out.println(month);

        // Updating variable's value!
        month = 5;

        System.out.println(month);

    }

}

package Day16;

public class Task2_NestedLoop {

    // print following pattern
    // *****
    // *****
    // *****
    // *****
    // *****
    // *****


    public static void main(String[] args) {


        for (int i = 0; i < 6; i++) {

            for (int j = 0; j < 5; j++) {
                System.out.print("*");
            }

            System.out.println();

        }

    }

}
